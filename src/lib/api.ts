// API Request with timeout - T√úM ƒ∞STEKLER PROXY √úZERƒ∞NDEN
const apiRequest = async (path: string, options: RequestInit = {}) => {
  try {
    const token = await getJWTToken();
    
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 10000);
    
    try {
      // üîß T√úM API ƒ∞STEKLERƒ∞ NETLIFY PROXY √úZERƒ∞NDEN (CORS sorunu √ß√∂z√ºm√º)
      // üö´ CACHE ENGELLEME - Sekme/Browser cache'i tamamen devre dƒ±≈üƒ±
      const response = await fetch('/.netlify/functions/api-proxy', {
        method: 'POST',
        headers: { 
          'Content-Type': 'application/json',
          'Cache-Control': 'no-cache, no-store, must-revalidate',
          'Pragma': 'no-cache',
          'Expires': '0'
        },
        body: JSON.stringify({
          path: `${path}${path.includes('?') ? '&' : '?'}_t=${Date.now()}`, // Timestamp cache buster
          method: options.method || 'GET',
          body: options.body ? JSON.parse(options.body as string) : undefined,
          jwtToken: token,
          apiKey: API_CONFIG.apiKey,
        }),
        signal: controller.signal
      });
      
      clearTimeout(timeoutId);
      
      const data = await response.json();
      if (!response.ok) {
        throw new Error(data.message || 'API Error');
      }
      
      console.log(`‚úÖ API SUCCESS (PROXY): ${options.method || 'GET'} ${path}`, data);
      return data;
    } catch (fetchError: any) {
      clearTimeout(timeoutId);
      if (fetchError.name === 'AbortError') {
        throw new Error('Request timeout - API yanƒ±t vermiyor');
      }
      console.error(`‚ùå API ERROR (PROXY): ${options.method || 'GET'} ${path}`, fetchError);
      throw fetchError;
    }
  } catch (error) {
    logError('API Request Error', error);
    throw error;
  }
};

