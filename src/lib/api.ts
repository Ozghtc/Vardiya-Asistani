// Hiyerar≈üik KULLANICI_ID √ºret - HIYERARSIK_ID_SISTEMI.md kurallarƒ±na uygun
const generateKullaniciId = async (kurum_id: string, departman_id: string, birim_id: string, rol: string): Promise<string> => {
  try {
    // Rol tipini belirle: admin=A, yonetici=Y, personel=P
    let rolKodu = 'P'; // Default: Personel
    if (rol === 'admin') rolKodu = 'A';
    else if (rol === 'yonetici') rolKodu = 'Y';

    // üî• SORUN √á√ñZ√úLDƒ∞: Parametreden gelen kurum_id YANLI≈û!
    // Veritabanƒ±ndan mevcut kurumlarƒ± √ßekip en y√ºksek ID'yi bulmalƒ±yƒ±z
    
    // 1. Mevcut kurumlarƒ± √ßek
    const kurumlar = await getKurumlar(true);
    console.log('üîç Mevcut kurumlar:', kurumlar.map((k: any) => k.kurum_id));
    
    // 2. En y√ºksek kurum_id'yi bul
    let maxKurumId = 0;
    kurumlar.forEach((kurum: any) => {
      const kurumIdNum = parseInt(kurum.kurum_id);
      if (kurumIdNum > maxKurumId) maxKurumId = kurumIdNum;
    });
    
    // 3. Yeni kurum_id olu≈ütur (sadece format i√ßin, kullanƒ±cƒ± mevcut kuruma ekleniyor)
    // UYARI: Kullanƒ±cƒ± mevcut bir kuruma ekleniyorsa, o kurumun ID'sini kullanmalƒ±yƒ±z!
    const actualKurumId = kurum_id; // Frontend'den se√ßilen mevcut kurum
    console.log(`üîç Kullanƒ±lacak kurum_id: ${actualKurumId} (frontend'den se√ßilen)`);

    // Mevcut kullanƒ±cƒ±larƒ± al ve aynƒ± birimde aynƒ± rol tipindeki en y√ºksek numarayƒ± bul
    const existingUsers = await getUsers(33); // kullanicilar_final tablosu
    
    // Aynƒ± birim ve rol tipindeki kullanƒ±cƒ±larƒ± filtrele
    const sameTypeUsers = existingUsers.filter((user: any) => 
      user.kurum_id === actualKurumId && 
      user.departman_id === departman_id && 
      user.birim_id === birim_id &&
      user.kullanici_id && 
      user.kullanici_id.includes(`_${rolKodu}`)
    );

    // En y√ºksek numarayƒ± bul
    let maxNumber = 0;
    sameTypeUsers.forEach((user: any) => {
      if (user.kullanici_id) {
        const match = user.kullanici_id.match(new RegExp(`_${rolKodu}(\\d+)$`));
        if (match) {
          const num = parseInt(match[1]);
          if (num > maxNumber) maxNumber = num;
        }
      }
    });

    // Yeni numara
    const newNumber = maxNumber + 1;
    
    // Hiyerar≈üik ID format: kurum_departman_birim_rolTipi+Numara
    // YENƒ∞ YAPI: Departman ve Birim kuruma baƒülƒ±
    // departman_id: "01_D1" -> "D1", birim_id: "01_B1" -> "B1"
    const departmanKodu = departman_id.split('_')[1]; // "01_D1" -> "D1"
    const birimKodu = birim_id.split('_')[1]; // "01_B1" -> "B1" (deƒüi≈üti!)
    const kullaniciId = `${actualKurumId}_${departmanKodu}_${birimKodu}_${rolKodu}${newNumber}`;
    
    console.log(`üÜî KULLANICI_ID olu≈üturuldu: ${kullaniciId}`);
    return kullaniciId;
    
  } catch (error) {
    console.error('KULLANICI_ID olu≈üturma hatasƒ±:', error);
    // Fallback: Timestamp bazlƒ± ID
    return `${kurum_id}_${Date.now()}_${rol.toUpperCase()[0]}1`;
  }
};

